def all_squares(max_number):
    '''
    Return a list of all the squares of numbers from 0 to max_number

    >>>all_squares(26)
    [0,1,2,3,4,5]
    '''
    new_list=[]
    for j in range(0,max_number):
        if j**2<=max_number:
            new_list.append(j**2)


